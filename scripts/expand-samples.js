function merge(file, extras) {
  const p = path.join("src", "data", file);
  const data = JSON.parse(fs.readFileSync(p, "utf8"));
  const seen = new Set(data.map((w) => w.word.toLowerCase()));
  let added = 0;
  for (const [word, meaning] of extras) {
    if (seen.has(word.toLowerCase())) continue;
    seen.add(word.toLowerCase());
    data.push({
      word,
      meaning,
      meanings: [meaning],
      pos: "",
      example: "",
      examples: [],
    });
    added++;
  }
  // 去掉空 example 字段污染
  const cleaned = data.map((w) => {
    const next = { ...w };
    if (!next.example) delete next.example;
    if (!next.examples || next.examples.length === 0 || !next.examples[0]) {
      delete next.examples;
      delete next.exampleTranslations;
    }
    if (!next.pos) delete next.pos;
    return next;
  });
  fs.writeFileSync(p, JSON.stringify(cleaned, null, 2) + "\n");
  console.log(`${file}: +${added} → ${cleaned.length}`);
}

